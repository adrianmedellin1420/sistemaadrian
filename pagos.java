    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        try
        {
            Statement st = cn.createStatement();
            String sql = "update Pedidos set Nombre = ?,Apellido  = ?,  Saldo= ?, Rebajar=?,AModelo=?, PrecioM=?, Fechaabono=?,  where N_cliente = ?";
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, Nombre.getText());
            pst.setString(2, Apellidos.getText());
            pst.setString(3, Saldo.getText());
            pst.setString(4, Rebajar.getText());
            pst.setString(5, AModelo.getText());
            pst.setString(6, PrecioM.getText());
            pst.setString(7, ((JTextField)Fechaabono.getDateEditor().getUiComponent()).getText());
            pst.setString(8, N_cliente.getText());
            int n = pst.executeUpdate();
            if (n > 0)
            {
                JOptionPane.showMessageDialog(this, "PEDIDO ACTUALIZADO CORRECTAMENTE");
                limpiar();
                vaciarTabla();
                verDatos();
                cliente.setEnabled(true);
                rebajar.setEnabled(false);
                editar.setEnabled(false);
                eliminar.setEnabled(false);

            }
        } catch (SQLException | HeadlessException e)
        {
            JOptionPane.showMessageDialog(this, "LOS DATOS NO HAN SIDO ACTUALIZADOS CORRECTAMENTE", "Error", JOptionPane.ERROR_MESSAGE);
        }           // TODO add your handling code here:
    }//GEN-LAST:event_editarActionPerformed

