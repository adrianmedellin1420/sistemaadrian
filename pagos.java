   public void vaciarTabla(){
        DefaultTableModel Modelo = (DefaultTableModel) pago.getModel();
        String titulos[] = {"CLAVE","NOMBRE","APELLIDO","SALDO","ABONO","SALDO NUEVO","MODELO","PRECIO MODELO","FECHA"};
        Modelo = new DefaultTableModel(null,titulos);
        pago.setModel(Modelo);
    }
    public void verDatos(){
        try {
           
            DefaultTableModel miModelo = (DefaultTableModel) pago.getModel();
            String dts[] = new String[9];
            String sql = "select * from Pagos";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                dts[0] = rs.getString("N_cliente");
                dts[1] = rs.getString("Nombre");
                dts[2] = rs.getString("Apellido");
                dts[3] = rs.getString("Saldo");
                dts[4] = rs.getString("Rebajar");
                dts[5] = rs.getString("AModelo");
                dts[6] = rs.getString("PrecioM");
                dts[7] = rs.getString("SaldoN");
                dts[8] = rs.getString("Fechaabono");
                miModelo.addRow(dts);
            }
            pago.setModel(miModelo);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "NO SE PUEDEN VISUALIZAR LOS DATOS DE LA TABLA", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
   
